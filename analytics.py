def geo_return(rets: np.ndarray, freq: int = 12) -> float:
    """Annualised geometric return from monthly percent returns."""
    n = len(rets)
    if n == 0:
        return np.nan
    growth = np.prod(1 + rets / 100)
    return (growth ** (freq / n) - 1) * 100


def ann_vol(rets: np.ndarray, freq: int = 12) -> float:
    """Annualised volatility of monthly percent returns."""
    if len(rets) < 2:
        return np.nan
    return float(np.std(rets, ddof=1) * np.sqrt(freq))


def sharpe_with_rf(df: pd.DataFrame) -> float:
    """Sharpe ratio using actual monthly TB3MS as risk-free rate."""
    ex = build_excess_returns(df)
    if len(ex) < 2:
        return np.nan
    v = np.std(ex, ddof=1)
    if v == 0:
        return 0.0
    return float(np.mean(ex) * np.sqrt(12) / v)


def sortino_with_rf(df: pd.DataFrame) -> float:
    """Sortino ratio using actual monthly TB3MS as MAR."""
    ex = build_excess_returns(df)
    if len(ex) < 2:
        return np.nan
    downside = ex[ex < 0]
    if len(downside) == 0:
        return np.inf
    dv = np.sqrt(np.mean(downside**2)) * np.sqrt(12)
    if dv == 0:
        return np.inf
    return float(np.mean(ex) * 12 / dv)


def max_drawdown(rets: np.ndarray) -> float:
    """Maximum drawdown as negative percent."""
    wealth = np.cumprod(1 + rets / 100)
    peak = np.maximum.accumulate(wealth)
    dd = (wealth - peak) / peak * 100
    return float(np.min(dd)) if len(dd) > 0 else 0.0


def drawdown_series(rets: np.ndarray) -> np.ndarray:
    """Full drawdown series as percent from peak."""
    wealth = np.cumprod(1 + rets / 100)
    peak = np.maximum.accumulate(wealth)
    return (wealth - peak) / peak * 100


def calmar(rets: np.ndarray) -> float:
    """Calmar ratio = annualised return / abs(max drawdown)."""
    md = abs(max_drawdown(rets))
    if md == 0:
        return np.nan
    return geo_return(rets) / md


def skewness(rets: np.ndarray) -> float:
    """Sample skewness (Fisher corrected)."""
    n = len(rets)
    if n < 3:
        return np.nan
    m = np.mean(rets)
    s = np.std(rets, ddof=1)
    if s == 0:
        return 0.0
    return float((n / ((n-1) * (n-2))) * np.sum(((rets - m) / s) ** 3))


def excess_kurtosis(rets: np.ndarray) -> float:
    """Sample excess kurtosis (Fisher corrected)."""
    n = len(rets)
    if n < 4:
        return np.nan
    m = np.mean(rets)
    s = np.std(rets, ddof=1)
    if s == 0:
        return 0.0
    s4 = np.sum(((rets - m) / s) ** 4)
    return float((n * (n+1)) / ((n-1)*(n-2)*(n-3)) * s4 - 3*(n-1)**2 / ((n-2)*(n-3)))


def jarque_bera(rets: np.ndarray) -> tuple[float, float]:
    """Jarque-Bera test statistic and p-value."""
    n = len(rets)
    s = skewness(rets)
    k = excess_kurtosis(rets)
    stat = n / 6 * (s**2 + k**2 / 4)
    pval = np.exp(-stat / 2)   # approximate, matches HTML version
    return float(stat), float(pval)


def compute_outliers(df: pd.DataFrame) -> pd.DataFrame:
    """Return rows where |ret - mean| > 3*std."""
    rets = df['ret'].values
    m = np.mean(rets)
    s = np.std(rets, ddof=1)
    return df[np.abs(rets - m) > 3 * s].copy()


# ─── NORMALITY TESTS ──────────────────────────────────────────────────────────

def normality_tests(rets: np.ndarray) -> dict:
    """Run Jarque-Bera, Shapiro-Wilk, Anderson-Darling, and KS tests."""
    results = {}

    # Jarque-Bera
    jb_stat, jb_p = jarque_bera(rets)
    results['jb'] = {'stat': jb_stat, 'pval': jb_p, 'name': 'Jarque-Bera'}

    # Shapiro-Wilk
    if len(rets) >= 3:
        sw_stat, sw_p = scipy_stats.shapiro(rets)
        results['sw'] = {'stat': float(sw_stat), 'pval': float(sw_p), 'name': 'Shapiro-Wilk'}

    # Anderson-Darling. scipy 1.17+ emits a FutureWarning nudging toward an
    # explicit `method`, but specifying one changes the result object from
    # `critical_values` to `pvalue` and would alter this test's logic. We keep
    # the statistic-vs-5%-critical-value test and silence the cosmetic warning;
    # the try/except still degrades gracefully if 1.19 removes critical_values.
    try:
        with warnings.catch_warnings():
            warnings.simplefilter('ignore', FutureWarning)
            ad_result = scipy_stats.anderson(rets, dist='norm')
        ad_stat = float(ad_result.statistic)
        # critical_values[2] is the 5% significance level
        ad_crit = float(ad_result.critical_values[2])
    except Exception:
        ad_stat, ad_crit = np.nan, np.nan
    results['ad'] = {
        'stat': ad_stat,
        'critical': ad_crit,
        'reject': ad_stat > ad_crit if not np.isnan(ad_stat) else False,
        'name': 'Anderson-Darling'
    }

    # Kolmogorov-Smirnov vs a fitted normal.
    # Computed directly rather than via scipy_stats.kstest(rets, <callable cdf>):
    # on some scipy 1.18 builds (notably macOS arm64) kstest routes a callable
    # CDF through an array-API path that crashes ("ndtr() takes 1-2 args but 3").
    # This is mathematically identical (empirical CDF vs fitted normal CDF) and
    # platform-independent — only norm.cdf on an array and kstwo.sf on scalars.
    _r = np.asarray(rets, dtype=float)
    _r = _r[~np.isnan(_r)]
    _n = len(_r)
    _mu, _sigma = float(np.mean(_r)), float(np.std(_r, ddof=1))
    _z = np.sort((_r - _mu) / _sigma)
    _cdf = scipy_stats.norm.cdf(_z)
    _d_plus = np.max(np.arange(1, _n + 1) / _n - _cdf)
    _d_minus = np.max(_cdf - np.arange(0, _n) / _n)
    ks_stat = float(max(_d_plus, _d_minus))
    ks_p = float(scipy_stats.kstwo.sf(ks_stat, _n))
    results['ks'] = {'stat': ks_stat, 'pval': ks_p, 'name': 'Kolmogorov-Smirnov'}

    return results


# ─── DRAWDOWN EPISODES ────────────────────────────────────────────────────────

def top_drawdowns(df: pd.DataFrame, n: int = 7) -> list[dict]:
    """Find top N drawdown episodes with peak/trough/recovery dates."""
    rets = df['ret'].values
    N = len(rets)

    # Build wealth index
    wealth = np.concatenate([[1.0], np.cumprod(1 + rets / 100)])

    episodes = []
    pk = 1.0
    pk_i = 0

    def close_episode(end_i):
        if end_i <= pk_i + 1:
            return
        tr = wealth[pk_i]
        tr_i = pk_i
        for j in range(pk_i + 1, end_i):
            if wealth[j] < tr:
                tr = wealth[j]
                tr_i = j
        if tr < pk:
            episodes.append({'dd': (tr/pk - 1)*100, 'pk_i': pk_i, 'tr_i': tr_i, 'rec_i': end_i - 1})

    for i in range(1, N + 1):
        if wealth[i] >= pk:
            close_episode(i)
            pk = wealth[i]
            pk_i = i

    if pk_i < N:
        tr = wealth[pk_i]
        tr_i = pk_i
        for j in range(pk_i + 1, N + 1):
            if wealth[j] < tr:
                tr = wealth[j]
                tr_i = j
        if tr < pk:
            episodes.append({'dd': (tr/pk - 1)*100, 'pk_i': pk_i, 'tr_i': tr_i, 'rec_i': None})

    episodes.sort(key=lambda e: e['dd'])

    MN = ['Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec']

    def fmt_date(idx):
        if idx is None:
            return 'Ongoing'
        if idx == 0:
            return 'Start'
        row = df.iloc[idx - 1]
        return f"{MN[int(row['month'])-1]} {int(row['year'])}"

    result = []
    for ep in episodes[:n]:
        result.append({
            'drawdown': ep['dd'],
            'peak_date': 'Start' if ep['pk_i'] == 0 else fmt_date(ep['pk_i']),
            'trough_date': fmt_date(ep['tr_i']),
            'recovery_date': fmt_date(ep['rec_i']),
            'peak_to_trough': ep['tr_i'] - ep['pk_i'],
            'trough_to_recovery': None if ep['rec_i'] is None else ep['rec_i'] - ep['tr_i'],
            'total_months': None if ep['rec_i'] is None else ep['rec_i'] - ep['pk_i'],
        })
    return result


# ─── REGRESSION ───────────────────────────────────────────────────────────────

def piecewise_beta_regression(fund_df: pd.DataFrame, bm_df: pd.DataFrame) -> Optional[dict]:
    """
    Full regression including piecewise beta, alpha, t-stats, SEs, confidence intervals.
    fund_df and bm_df must have aligned {year, month, ret} rows.
    """
    # Align on common (year, month)
    merged = fund_df.merge(bm_df, on=['year','month'], suffixes=('_fund','_bm'))
    if len(merged) < 5:
        return None

    y = merged['ret_fund'].values
    x = merged['ret_bm'].values
    n = len(x)

    mx, my = np.mean(x), np.mean(y)
    sx = np.std(x, ddof=1)
    sy = np.std(y, ddof=1)
    if sx == 0 or sy == 0:
        return None

    # OLS beta and alpha
    cov = np.sum((x - mx) * (y - my)) / (n - 1)
    varx = np.sum((x - mx)**2) / (n - 1)
    beta = cov / varx
    alpha = my - beta * mx
    corr = cov / (sx * sy)
    r2 = corr**2

    # Regression standard errors and t-stats
    y_hat = alpha + beta * x
    resid = y - y_hat
    sse = np.sum(resid**2)
    mse = sse / (n - 2) if n > 2 else np.nan
    se_beta = np.sqrt(mse / ((n-1) * varx)) if mse is not np.nan else np.nan
    se_alpha = np.sqrt(mse * (1/n + mx**2 / ((n-1)*varx))) if mse is not np.nan else np.nan
    t_beta  = beta / se_beta if se_beta and se_beta > 0 else np.nan
    t_alpha = alpha / se_alpha if se_alpha and se_alpha > 0 else np.nan

    # 95% confidence intervals
    t_crit = scipy_stats.t.ppf(0.975, df=n-2) if n > 2 else np.nan
    alpha_ci = (alpha - t_crit*se_alpha, alpha + t_crit*se_alpha) if not np.isnan(t_crit) else (np.nan, np.nan)
    beta_ci  = (beta  - t_crit*se_beta,  beta  + t_crit*se_beta)  if not np.isnan(t_crit) else (np.nan, np.nan)

    # Piecewise betas
    up_mask = x > 0
    dn_mask = x <= 0

    def piecewise_beta(xp, yp):
        if len(xp) < 3:
            return None
        pmx, pmy = np.mean(xp), np.mean(yp)
        pc = np.sum((xp-pmx)*(yp-pmy)) / (len(xp)-1)
        pv = np.sum((xp-pmx)**2) / (len(xp)-1)
        return 0.0 if pv == 0 else pc/pv

    beta_up = piecewise_beta(x[up_mask], y[up_mask])
    beta_dn = piecewise_beta(x[dn_mask], y[dn_mask])
    convexity = (beta_up - beta_dn) if (beta_up is not None and beta_dn is not None) else None

    return {
        'n': n,
        'beta': beta, 'alpha': alpha, 'corr': corr, 'r2': r2,
        'se_beta': se_beta, 'se_alpha': se_alpha,
        't_beta': t_beta, 't_alpha': t_alpha,
        'alpha_ci': alpha_ci, 'beta_ci': beta_ci,
        'beta_up': beta_up, 'beta_dn': beta_dn, 'convexity': convexity,
        'x': x, 'y': y,
        'up_x': x[up_mask], 'up_y': y[up_mask],
        'dn_x': x[dn_mask], 'dn_y': y[dn_mask],
        'n_up': int(up_mask.sum()), 'n_dn': int(dn_mask.sum()),
    }


# ─── AUTOCORRELATION ──────────────────────────────────────────────────────────

def acf(rets: np.ndarray, max_lag: int = 12) -> list[float]:
    """Return autocorrelation at lags 1..max_lag."""
    n = len(rets)
    m = np.mean(rets)
    v = np.sum((rets - m)**2) / n
    result = []
    for lag in range(1, max_lag + 1):
        cov = np.sum((rets[lag:] - m) * (rets[:n-lag] - m)) / n
        result.append(0.0 if v == 0 else cov / v)
    return result


def acf_conf_band(n: int) -> float:
    """95% confidence band for autocorrelation ≈ 1.96/sqrt(n)."""
    return 1.96 / np.sqrt(n)


# ─── Q-Q DATA ─────────────────────────────────────────────────────────────────

def qq_data(rets: np.ndarray) -> dict:
    """Return sorted data and theoretical quantiles for Q-Q plot."""
    n = len(rets)
    sorted_r = np.sort(rets)
    probs = (np.arange(1, n+1) - 0.5) / n
    theoretical = scipy_stats.norm.ppf(probs, loc=np.mean(rets), scale=np.std(rets, ddof=1))
    return {'data': sorted_r, 'theoretical': theoretical}


# ─── MULTI-PERIOD STATS ───────────────────────────────────────────────────────

def period_stats(df: pd.DataFrame) -> Optional[dict]:
    """Compute all stats for a slice of data."""
    if df is None or len(df) == 0:
        return None
    rets = df['ret'].values
    return {
        'n': len(rets),
        'total_ret': (np.prod(1 + rets/100) - 1) * 100,
        'ann_ret': geo_return(rets),
        'ann_vol': ann_vol(rets),
        'sharpe': sharpe_with_rf(df),
        'sortino': sortino_with_rf(df),
        'max_dd': max_drawdown(rets),
        'calmar': calmar(rets),
        'hit_rate': float(np.sum(rets > 0) / len(rets) * 100),
        'best': float(np.max(rets)),
        'worst': float(np.min(rets)),
        'skew': skewness(rets),
        'exkurt': excess_kurtosis(rets),
    }


# ─── ROLLING METRICS ──────────────────────────────────────────────────────────

def rolling_metrics(df: pd.DataFrame, window: int = 12) -> pd.DataFrame:
    """Compute rolling 12-month return, Sharpe, and volatility."""
    records = []
    for i in range(window - 1, len(df)):
        sub = df.iloc[i - window + 1 : i + 1].copy()
        rets = sub['ret'].values
        records.append({
            'year': int(df.iloc[i]['year']),
            'month': int(df.iloc[i]['month']),
            'roll_ret': geo_return(rets),
            'roll_vol': ann_vol(rets),
            'roll_sharpe': sharpe_with_rf(sub),
        })
    return pd.DataFrame(records)


# ─── SEASONALITY ──────────────────────────────────────────────────────────────

def seasonality(df: pd.DataFrame) -> dict:
    """Monthly and quarterly average returns."""
    monthly = df.groupby('month')['ret'].agg(['mean','std','count']).reset_index()
    monthly.columns = ['month', 'mean', 'std', 'count']

    df2 = df.copy()
    df2['quarter'] = ((df2['month'] - 1) // 3) + 1
    quarterly = df2.groupby('quarter')['ret'].agg(['mean','std','count']).reset_index()
    quarterly.columns = ['quarter', 'mean', 'std', 'count']

    return {'monthly': monthly, 'quarterly': quarterly}


# ─── MACRO EVENTS ─────────────────────────────────────────────────────────────

def compound_return(series_df: pd.DataFrame, months: list[tuple]) -> float:
    """Compound return over specified (year, month) tuples."""
    p = 1.0
    for (y, m) in months:
        row = series_df[(series_df['year'] == y) & (series_df['month'] == m)]
        if len(row) > 0:
            p *= (1 + row.iloc[0]['ret'] / 100)
    return (p - 1) * 100


def macro_events_table(fund_df: pd.DataFrame, bm_df: Optional[pd.DataFrame] = None) -> list[dict]:
    """Build macro events stress-test table."""
    rows = []
    for ev in MACRO_EVENTS:
        months = ev['months']
        # Only include event if fund has data for at least one of those months
        has_data = any(
            len(fund_df[(fund_df['year'] == y) & (fund_df['month'] == m)]) > 0
            for (y, m) in months
        )
        if not has_data:
            continue
        fund_ret = compound_return(fund_df, months)
        bm_ret = compound_return(bm_df, months) if bm_df is not None else None
        spread = (fund_ret - bm_ret) if bm_ret is not None else None
        rows.append({
            'name': ev['name'],
            'period': ev['period'],
            'fund_ret': fund_ret,
            'bm_ret': bm_ret,
            'spread': spread,
        })
    return rows


# ─── CSV/EXCEL INGESTION ──────────────────────────────────────────────────────
#
# Goal: accept any reasonable CSV or Excel file of monthly returns and produce
# a "golden copy" DataFrame with exactly three columns:
#   year  (int)   — calendar year
#   month (int)   — 1..12
#   ret   (float) — monthly return in percent (e.g. 1.23 means +1.23%)
#
# Design principles:
#   1. Never import inside a function — all imports at module top level.
#   2. Explicit beats implicit — try every known date format before falling back.
#   3. Return diagnostics so the UI can show the user what was detected.
#   4. One public entry point: parse_uploaded_file().

_MM_YYYY = re.compile(r'^(\d{1,2})[/\-.](\d{4})$')

_DATE_FORMATS = [
    '%b-%Y',    # Jul-2005
    '%b %Y',    # Jul 2005
    '%b/%Y',    # Jul/2005
    '%B-%Y',    # July-2005
    '%B %Y',    # July 2005
    '%Y-%m',    # 2005-07
    '%Y/%m',    # 2005/07
    '%Y-%m-%d', # 2005-07-01
    '%m/%d/%Y', # 07/01/2005
    '%d/%m/%Y', # 01/07/2005
    '%d-%m-%Y', # 01-07-2005
    '%d.%m.%Y', # 01.07.2005
    '%d %b %Y', # 31 Jul 2005
    '%d %B %Y', # 31 July 2005
]


def _clean_date_strings(series):
    """Normalise raw date strings before format matching."""
    s = series.astype(str).str.strip()
    s = s.str.replace(r'\s+', ' ', regex=True)
    s = s.str.replace('\u2013', '-', regex=False)  # en dash
    s = s.str.replace('\u2014', '-', regex=False)  # em dash
    return s


def _parse_date_series(series):
    """
    Parse a Series of date values into a pandas datetime Series.
    Returns (datetime_series, format_label).
    Raises ValueError if no format achieves >= 80% parse rate.
    """
    # Already datetime — nothing to do
    if pd.api.types.is_datetime64_any_dtype(series):
        return series, 'datetime64'

    # Numeric — try Excel serial date (days since 1899-12-30)
    if pd.api.types.is_numeric_dtype(series):
        converted = pd.to_datetime(
            series, unit='D', origin='1899-12-30', errors='coerce'
        )
        if converted.notna().sum() >= len(series) * 0.8:
            return converted, 'excel_serial'

    cleaned = _clean_date_strings(series)
    n = max(len(cleaned), 1)

    # Detect MM/YYYY, MM-YYYY, MM.YYYY explicitly (pandas gets these wrong)
    sample = [v for v in cleaned.head(10).tolist() if v not in ('nan', '', 'None')]
    if sample and all(_MM_YYYY.match(v) for v in sample):
        def _to_ts(v):
            m = _MM_YYYY.match(str(v))
            if m:
                try:
                    return pd.Timestamp(year=int(m.group(2)), month=int(m.group(1)), day=1)
                except ValueError:
                    return pd.NaT
            return pd.NaT
        parsed = cleaned.map(_to_ts)
        if parsed.notna().sum() >= n * 0.8:
            return parsed, 'MM/YYYY'

    # Try every explicit format
    for fmt in _DATE_FORMATS:
        parsed = pd.to_datetime(cleaned, format=fmt, errors='coerce')
        if parsed.notna().sum() >= n * 0.8:
            return parsed, fmt

    # Last resort: let pandas infer
    parsed = pd.to_datetime(cleaned, errors='coerce')
    if parsed.notna().sum() >= n * 0.8:
        return parsed, 'inferred'

    raise ValueError(
        'Could not parse date column. Sample values: '
        + str(series.dropna().head(3).tolist())
    )


def _parse_return_series(series):
    """
    Parse a Series of return values into float percent.
    Handles: '1.23%', 1.23, 0.0123 (decimal), '(1.23)' (negative parens).
    Returns (float_series_in_percent, format_label).
    """
    if pd.api.types.is_numeric_dtype(series):
        vals = series.astype(float)
    else:
        s = series.astype(str).str.strip()
        s = s.str.replace('%', '', regex=False)
        s = s.str.replace(',', '', regex=False)
        s = s.str.replace(r'[\$\u00a3\u20ac]', '', regex=True)  # $, £, €
        s = s.str.replace(r'^\((.+)\)$', r'-\1', regex=True)   # (1.23) -> -1.23
        vals = pd.to_numeric(s, errors='coerce')

    non_nan = vals.dropna()
    if len(non_nan) == 0:
        raise ValueError('Return column contains no numeric values.')

    # If every absolute value < 1, assume decimal format and convert to percent
    if (non_nan.abs() < 1.0).all():
        return vals * 100, 'decimal x100'
    return vals, 'percent'


def parse_uploaded_file(uploaded_file):
    """
    Parse a CSV or Excel file of monthly returns.

    Returns
    -------
    df   : pd.DataFrame | None  — golden copy with columns year, month, ret
    err  : str                  — non-empty string if parsing failed
    diag : dict                 — what was detected (columns, formats, row counts)
    """
    diag = {}

    # ── 1. Read the raw file ───────────────────────────────────────────────────
    try:
        fname = uploaded_file.name.lower()
        if fname.endswith('.csv'):
            raw = pd.read_csv(uploaded_file, thousands=',')
        elif fname.endswith(('.xlsx', '.xls')):
            raw = pd.read_excel(uploaded_file, thousands=',')
        else:
            return None, 'Unsupported file type. Please upload .csv, .xlsx, or .xls.', diag
    except Exception as exc:
        return None, f'Could not read file: {exc}', diag

    raw = raw.dropna(how='all').dropna(axis=1, how='all').reset_index(drop=True)
    if raw.empty:
        return None, 'File is empty after removing blank rows.', diag
    diag['n_raw'] = len(raw)

    # ── 2. Identify date column ────────────────────────────────────────────────
    date_kw = ['date', 'month', 'period', 'time', 'year', 'as of', 'asof']
    date_col = None
    for col in raw.columns:
        if any(kw in str(col).lower() for kw in date_kw):
            date_col = col
            break
    if date_col is None:
        # First non-numeric column
        for col in raw.columns:
            if not pd.api.types.is_numeric_dtype(raw[col]):
                date_col = col
                break
    if date_col is None:
        date_col = raw.columns[0]
    diag['date_col'] = str(date_col)

    # ── 3. Identify return column ──────────────────────────────────────────────
    ret_kw = ['return', 'ret', 'perf', 'pnl', 'nav', 'gain', 'loss', 'p&l', 'monthly']
    ret_col = None
    for col in raw.columns:
        if col == date_col:
            continue
        if any(kw in str(col).lower() for kw in ret_kw):
            ret_col = col
            break
    if ret_col is None:
        for col in raw.columns:
            if col == date_col:
                continue
            if pd.api.types.is_numeric_dtype(raw[col]):
                ret_col = col
                break
    if ret_col is None:
        for col in raw.columns:
            if col == date_col:
                continue
            probe = pd.to_numeric(
                raw[col].astype(str).str.replace('%', '').str.strip(),
                errors='coerce'
            )
            if probe.notna().sum() > len(raw) * 0.5:
                ret_col = col
                break
    if ret_col is None:
        return None, 'Could not identify a return column.', diag
    diag['ret_col'] = str(ret_col)

    # ── 4. Parse dates ─────────────────────────────────────────────────────────
    try:
        dates, date_fmt = _parse_date_series(raw[date_col])
    except ValueError as exc:
        return None, str(exc), diag
    diag['date_format'] = date_fmt
    diag['failed_dates'] = raw[date_col][dates.isna()].astype(str).tolist()

    # ── 5. Parse returns ───────────────────────────────────────────────────────
    try:
        rets, ret_fmt = _parse_return_series(raw[ret_col])
    except ValueError as exc:
        return None, str(exc), diag
    diag['ret_format'] = ret_fmt

    # ── 6. Assemble and validate golden copy ───────────────────────────────────
    golden = pd.DataFrame({
        'year':  dates.dt.year,
        'month': dates.dt.month,
        'ret':   rets,
    })
    n_before = len(golden)
    golden = golden.dropna()
    golden = golden.astype({'year': int, 'month': int, 'ret': float})
    golden = golden.sort_values(['year', 'month']).reset_index(drop=True)
    golden = golden.drop_duplicates(subset=['year', 'month'], keep='last')

    diag['n_parsed'] = len(golden)
    diag['n_dropped'] = n_before - len(golden)

    if len(golden) < 6:
        return None, (
            f'Only {len(golden)} valid rows found (need ≥ 6). '
            f'Date column: "{date_col}" ({date_fmt}), '
            f'Return column: "{ret_col}" ({ret_fmt}).'
        ), diag

    return golden, '', diag


# ─── EXEC SUMMARY & DDQ ───────────────────────────────────────────────────────
# Allocator-voice synthesis and a Socratic due-diligence questionnaire, both
# generated from the loaded return stream and its computed analytics.

MN = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun',
      'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec']


def _exec_metrics(fund_df, mkt_df):
    r = fund_df['ret'].values.astype(float)
    n = len(r)
    m = {'n': n}
    if n == 0:
        return m
    fd = fund_df.reset_index(drop=True)
    m['y0'], m['m0'] = int(fd.iloc[0]['year']), int(fd.iloc[0]['month'])
    m['y1'], m['m1'] = int(fd.iloc[-1]['year']), int(fd.iloc[-1]['month'])
    m['years'] = n / 12.0
    m['ann'] = geo_return(r); m['vol'] = ann_vol(r)
    m['sharpe'] = sharpe_with_rf(fund_df); m['sortino'] = sortino_with_rf(fund_df)
    m['mdd'] = max_drawdown(r); m['calmar'] = calmar(r)
    bi, wi = int(np.argmax(r)), int(np.argmin(r))
    m['best'] = float(r[bi]); m['worst'] = float(r[wi])
    m['best_m'] = f"{MN[int(fd.iloc[bi]['month'])-1]} {int(fd.iloc[bi]['year'])}"
    m['worst_m'] = f"{MN[int(fd.iloc[wi]['month'])-1]} {int(fd.iloc[wi]['year'])}"
    m['hit'] = float((r > 0).mean() * 100)
    m['skew'] = skewness(r); m['kurt'] = excess_kurtosis(r)
    _, m['jb_p'] = jarque_bera(r)
    m['lag1'] = acf(r, 1)[0] if n > 3 else 0.0
    m['reg'] = piecewise_beta_regression(fund_df, mkt_df)
    m['dd'] = top_drawdowns(fund_df, 3)
    m['macro'] = macro_events_table(fund_df, mkt_df)
    return m


def build_exec_summary(fund_df, fund_name, mkt_df, mkt_name,
                       agg_df=None, bm3_df=None, bm3_name=''):
    """Return a list of (subhead, html_body) blocks — an allocator's review."""
    m = _exec_metrics(fund_df, mkt_df)
    if m['n'] == 0:
        return [("Executive Summary", "<p style='color:#1A1A1A'>No return data is loaded.</p>")]

    def P(*sentences):
        return ("<p style='color:#1A1A1A;font-size:15px;line-height:1.6;margin:0 0 10px 0;'>"
                + " ".join(sentences) + "</p>")

    span = f"{MN[m['m0']-1]} {m['y0']} \u2013 {MN[m['m1']-1]} {m['y1']}"
    blocks = []

    # 1 — Snapshot
    blocks.append(("Snapshot", P(
        f"The record under review spans {span} ({m['n']} monthly observations, ~{m['years']:.1f} years).",
        f"Over that window the strategy compounded at {m['ann']:.2f}% annualized with {m['vol']:.2f}% annualized volatility,",
        f"producing a Sharpe of {m['sharpe']:.2f} and a Sortino of {m['sortino']:.2f} (risk-free = 3M T-bills).",
        f"{m['hit']:.2f}% of months were positive.")))

    # 2 — Drawdowns
    dd_txt = [f"The deepest peak-to-trough drawdown was {m['mdd']:.2f}%, giving a Calmar ratio of {m['calmar']:.2f}."]
    if m['dd']:
        d0 = m['dd'][0]
        rec = d0.get('recovery_date') or d0.get('recovery') or 'not yet recovered'
        dd_txt.append(f"The worst episode ran from {d0['peak_date']} to a trough in {d0['trough_date']} ({d0['drawdown']:.2f}%), recovering by {rec}.")
    dd_txt.append("An allocator should size any position against the prospect of repeating that drawdown, not the headline return.")
    blocks.append(("Risk & Drawdowns", P(*dd_txt)))

    # 3 — Market sensitivity
    reg = m['reg']
    if reg:
        cap = []
        cap.append(f"Against {mkt_name}, beta is {reg['beta']:.2f} with a monthly alpha of {reg['alpha']:.2f}% (R\u00b2 = {reg['r2']:.2f}, correlation {reg['corr']:.2f}).")
        if reg.get('beta_up') is not None and reg.get('beta_dn') is not None:
            updn = "more in up-markets than it gives back in down-markets" if reg['beta_up'] > reg['beta_dn'] else "less in up-markets than it loses in down-markets"
            cap.append(f"Up-market beta is {reg['beta_up']:.2f} versus {reg['beta_dn']:.2f} in down-markets \u2014 it captures {updn}.")
        cap.append("The question is whether that alpha is a durable edge or compensation for a risk not visible in this monthly series.")
        blocks.append(("Market Sensitivity", P(*cap)))

    # 4 — Tail & distribution
    skew_txt = "a left tail (downside surprises larger than upside)" if m['skew'] < 0 else "a right tail (upside surprises larger than downside)"
    fat = "fat-tailed relative to a normal distribution" if m['kurt'] > 0.5 else "close to normal in its tails"
    norm = "rejects normality" if m['jb_p'] < 0.05 else "does not reject normality"
    blocks.append(("Tail Behaviour", P(
        f"Monthly returns show skew of {m['skew']:.2f} ({skew_txt}) and excess kurtosis of {m['kurt']:.2f} ({fat}).",
        f"The Jarque-Bera test {norm} at the 5% level.",
        f"The best and worst months were {m['best']:.2f}% ({m['best_m']}) and {m['worst']:.2f}% ({m['worst_m']}); confirm neither was a single position or a one-off mark.")))

    # 5 — Smoothing flag
    if m['lag1'] >= 0.2:
        smooth = (f"First-order autocorrelation is {m['lag1']:.2f}, which is elevated. "
                  "Persistent positive autocorrelation can indicate illiquid or stale-priced holdings whose marks lag, "
                  "flattering reported volatility and Sharpe. This warrants direct questions on valuation and liquidity.")
    else:
        smooth = (f"First-order autocorrelation is {m['lag1']:.2f}, low enough that return smoothing from stale marks is unlikely to be materially distorting the risk statistics.")
    blocks.append(("Return Smoothing", P(smooth)))

    # 6 — Crisis behaviour
    if m['macro']:
        prot = [e for e in m['macro'] if e.get('spread') is not None and e['spread'] > 0]
        worst_ev = min((e for e in m['macro'] if e.get('fund_ret') is not None), key=lambda e: e['fund_ret'], default=None)
        ctxt = [f"Across the {len(m['macro'])} stress windows that overlap this track, the strategy outperformed {mkt_name} in {len(prot)} of them."]
        if worst_ev:
            ctxt.append(f"Its hardest event was {worst_ev['name']} ({worst_ev['period']}) at {worst_ev['fund_ret']:.2f}%.")
        ctxt.append("Crisis behaviour, not full-cycle averages, is where capital is preserved or lost.")
        blocks.append(("Crisis Behaviour", P(*ctxt)))

    # 7 — Bottom line
    verdict = []
    if m['sharpe'] >= 1.0 and m['mdd'] > -25:
        verdict.append("On the numbers alone the risk-adjusted profile is attractive.")
    elif m['sharpe'] >= 0.5:
        verdict.append("On the numbers the risk-adjusted profile is reasonable but not exceptional.")
    else:
        verdict.append("On the numbers the risk-adjusted profile is modest and the return appears largely market-driven.")
    verdict.append("Before allocating, the track record's length, the source of alpha, the valuation of any illiquid book, "
                   "and the firm's people and operations all need to be tested directly \u2014 see the DDQ tab. "
                   "Past performance computed from a monthly series cannot, by itself, support a commitment.")
    blocks.append(("Allocator's Bottom Line", P(*verdict)))
    return blocks


def build_ddq(fund_df, fund_name, mkt_df):
    """Return [(category, [(question, socratic_type), ...]), ...]. Socratic types:
    Clarify, Assumptions, Evidence, Perspective, Implications, Meta."""
    m = _exec_metrics(fund_df, mkt_df)
    worst_m = m.get('worst_m', 'the worst month'); worst = m.get('worst', 0.0)
    best_m = m.get('best_m', 'the best month'); best = m.get('best', 0.0)
    mdd = m.get('mdd', 0.0); lag1 = m.get('lag1', 0.0)
    reg = m.get('reg') or {}
    beta = reg.get('beta')

    cats = []

    cats.append(("A. Investment Thesis & Edge", [
        ("In your own words, what specific market inefficiency does the strategy exploit, and why does it persist?", "Clarify"),
        ("What must be true about market structure or participant behaviour for that edge to keep working?", "Assumptions"),
        ("What evidence, beyond returns, would convince a skeptic the edge is real rather than a factor exposure?", "Evidence"),
        ("How would a competitor running the opposite view describe what you do, and where might they be right?", "Perspective"),
        ("If your primary edge were arbitraged away tomorrow, what in the process would still generate return?", "Implications"),
    ]))

    cats.append(("B. Performance & Attribution", [
        (f"Walk us through {best_m}, the best month at {best:.2f}% \u2014 what drove it, and was it repeatable or idiosyncratic?", "Clarify"),
        (f"In {worst_m} the strategy returned {worst:.2f}%; what was the proximate cause and what changed afterward?", "Evidence"),
        ("What portion of lifetime return is attributable to a handful of positions, and what does that imply about breadth?", "Implications"),
        ("Which assumptions in your attribution would most change the story if they were wrong?", "Assumptions"),
        ("How do you distinguish skill from beta in your own reporting, and would an independent reviewer agree?", "Perspective"),
    ]))

    risk_q = [
        (f"The deepest drawdown in this record is {mdd:.2f}%; what was your peak gross and net exposure into and through it?", "Clarify"),
        ("What are the explicit, pre-committed rules that force de-risking, and when were they last triggered?", "Evidence"),
        ("What scenario keeps you up at night that your current risk limits would not catch?", "Assumptions"),
        ("If a position gapped against you overnight at 3x your VaR, what exactly happens next, step by step?", "Implications"),
    ]
    if beta is not None:
        risk_q.append((f"Realized beta to the market is {beta:.2f}; how much of your return survives if you hedge that beta out?", "Perspective"))
    cats.append(("C. Risk Management & Drawdowns", risk_q))

    cats.append(("D. Portfolio Construction, Liquidity & Capacity", [
        ("How many days to liquidate the book at 20% of ADV without moving prices, in both calm and stressed markets?", "Clarify"),
        ("What is the strategy's honest capacity, and what evidence from your own trading supports that number?", "Evidence"),
        ("What assumption about market liquidity is embedded in your position sizing, and when has it failed?", "Assumptions"),
        ("If assets doubled, which part of the process degrades first?", "Implications"),
    ]))

    smooth_note = (f"first-order autocorrelation of monthly returns is {lag1:.2f}"
                   + (" \u2014 elevated, which can signal stale marks" if lag1 >= 0.2 else ""))
    cats.append(("E. Valuation, Marks & Return Smoothing", [
        (f"Our analysis shows {smooth_note}; how are hard-to-value positions priced, and by whom?", "Evidence"),
        ("Who independently verifies marks, how often, and what happens when their price disagrees with yours?", "Clarify"),
        ("What assumptions underlie your fair-value process for the least liquid 10% of the book?", "Assumptions"),
        ("If a third party re-priced the portfolio at quarter-end, where would the largest differences appear, and why?", "Implications"),
    ]))

    cats.append(("F. Organization, Team & Key-Person Succession", [
        ("Who are the decision-makers, and which single departure would most damage the franchise?", "Clarify"),
        ("What is the written succession plan for the CIO and other key persons, and who has seen it?", "Evidence"),
        ("What assumptions about staff retention underpin your continuity \u2014 vesting, equity, non-competes?", "Assumptions"),
        ("From a departing partner's perspective, what would make them leave, and how is that risk mitigated?", "Perspective"),
        ("If the founder were unavailable for six months, what concretely changes in how capital is managed?", "Implications"),
    ]))

    cats.append(("G. Operations, Counterparties & Disaster Recovery", [
        ("Who are your prime brokers, administrator and auditor, and when did each last change?", "Clarify"),
        ("Describe your business-continuity and disaster-recovery plan \u2014 when was it last tested, and what failed?", "Evidence"),
        ("What assumptions does your operational setup make about counterparty solvency and access to financing?", "Assumptions"),
        ("If your primary prime broker failed on a Friday, what is the state of the portfolio on Monday?", "Implications"),
        ("Where is the line between investment risk and operational risk in your own org chart, and who owns each?", "Meta"),
    ]))

    cats.append(("H. Compliance, Alignment & Terms", [
        ("How much of the principals' liquid net worth is invested alongside LPs, and on the same terms?", "Clarify"),
        ("What conflicts of interest exist across vehicles or share classes, and how are they disclosed and managed?", "Evidence"),
        ("What assumptions about fee and liquidity terms are baked into your reported net returns for this class?", "Assumptions"),
        ("Are we asking the right questions to understand this strategy, or is there a question we should be asking that we are not?", "Meta"),
    ]))
    return cats
